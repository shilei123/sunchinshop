package cn.sqhl.shop.vo;

public class CategoryPropertyValue {
    private String id;

    private String category_id;
    
    private String property;
    
    private String property_id;
    
    private String property_code;
    
    private String propertyvalue;
    
    private String propertyvalue_id;
    
    private String propertyvalue_code;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCategory_id() {
		return category_id;
	}

	public void setCategory_id(String category_id) {
		this.category_id = category_id;
	}

	public String getProperty() {
		return property;
	}

	public void setProperty(String property) {
		this.property = property;
	}

	public String getProperty_id() {
		return property_id;
	}

	public void setProperty_id(String property_id) {
		this.property_id = property_id;
	}

	public String getProperty_code() {
		return property_code;
	}

	public void setProperty_code(String property_code) {
		this.property_code = property_code;
	}

	public String getPropertyvalue() {
		return propertyvalue;
	}

	public void setPropertyvalue(String propertyvalue) {
		this.propertyvalue = propertyvalue;
	}

	public String getPropertyvalue_id() {
		return propertyvalue_id;
	}

	public void setPropertyvalue_id(String propertyvalue_id) {
		this.propertyvalue_id = propertyvalue_id;
	}

	public String getPropertyvalue_code() {
		return propertyvalue_code;
	}

	public void setPropertyvalue_code(String propertyvalue_code) {
		this.propertyvalue_code = propertyvalue_code;
	}

}